    private void logar(){
        admDTO.setEmail(email.getText());
        admDTO.setSenha(String.valueOf(senha.getPassword()));
        admDTO.setCodadm(admCTR.logarAdm(admDTO));
        if(admDTO.getCodadm()> 0){
            this.dispose();
            ConquistaVIEW conquistaVIEW = new ConquistaVIEW();
            PrincipalVIEW.desktopPane.add(conquistaVIEW);
            conquistaVIEW.setVisible(true);
        }
        else{
            JOptionPane.showMessageDialog(null, "Login ou senha\nIncorreto!!!");
        }
    }//Fecha m√©todo logar()

